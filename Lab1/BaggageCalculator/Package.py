class package:
    length=0
    width=0
    height=0
    weight=0
    def __init__(self,length,width,height,weight):
        self.length=length
        self.width=width
        self.height=height
        self.weight=weight
    def getLength(self):
        return self.length
    def getWidth(self):
        return self.width
    def getHeight(self):
        return self.height
    def getWeight(self):
        return self.weight